package com.aspire.sqmp.mobilemanager.service.request;

/**
 * The class <code>AspRequest</code>
 * 
 * @author wuyanlong
 * @version 1.0
 */
public abstract class AspMessage implements AspCommand {
    /**
     * Command
     */
    protected int command;

    /**
     * Constructor
     * 
     * @param command
     *            the command of {@link AspCommand}
     */
    public AspMessage() {
    }

    /**
     * Constructor
     * 
     * @param command
     *            the command of {@link AspCommand}
     */
    public AspMessage(int command) {
        this.command = command;

    }

    /**
     * Getter of command
     * 
     * @return the command
     */
    public int getCommand() {
        return command;
    }
}
