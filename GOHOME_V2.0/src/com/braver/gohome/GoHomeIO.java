package com.braver.gohome;

/**
 * The class <code>GoHomeIO</code>
 * 
 * @author braver
 * @version 1.0
 */
public interface GoHomeIO {

	/**
	 * 输出信息
	 * 
	 * @param str
	 */
	public void write(String str);

	/**
	 * 输出信息
	 * 
	 * @param str
	 */
	public void writeln(String str);

	/**
	 * 输出错误信息
	 * 
	 * @param str
	 */
	public void writeError(String str);

	/**
	 * 输出错误信息
	 * 
	 * @param str
	 */
	public void writeErrorLn(String str);

	/**
	 * 输入信息
	 * 
	 * @return
	 */
	public String readLine();

	/**
	 * 输入信息
	 * 
	 * @return
	 */
	public int readInt();

	/**
	 * 写消息
	 * 
	 * @param message
	 */
	public void writeMessage(Message message);

}
