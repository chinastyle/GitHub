package com.aspire.mose.frame.net.connect;

import java.io.IOException;

/**
 * 网络输入通知处理接口 从网络连接的输入流（服务器发送过来的数据），获取到数据后，调用这个监听器的处理函数
 * @author liangbo
 *
 * 2011-7-7 下午04:35:36
 *  
 * IConnectInputListener
 *
 */
public interface IConnectInputListener {
	
	/**
	 * 网络输入数据处理接口
	 * @param b 网络层输入的数据
	 * @throws IOException IO异常
	 */
	public void receive(byte[] b) throws IOException;
	
}
