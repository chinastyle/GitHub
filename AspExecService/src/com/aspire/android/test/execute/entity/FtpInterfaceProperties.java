package com.aspire.android.test.execute.entity;

import com.thoughtworks.xstream.annotations.XStreamAlias;

/**
 * The class <code>FtpProperties</code>
 *
 * @author likai
 * @version 1.0
 */
public class FtpInterfaceProperties {

    @XStreamAlias("ftpIp")
    private String ftpIp;

    @XStreamAlias("ftpPort")
    private String ftpPort;

    @XStreamAlias("ftpPath")
    private String ftpPath;

    @XStreamAlias("ftpUserName")
    private String ftpUserName;

    @XStreamAlias("ftpPwd")
    private String ftpPwd;

    @XStreamAlias("ftpDownloadInterval")
    private String ftpDownloadInterval;

    @XStreamAlias("ftpUploadInterval")
    private String ftpUploadInterval;

    /**
     * Getter of ftpIp
     * @return the ftpIp
     */
    public String getFtpIp() {
        return ftpIp;
    }

    /**
     * Setter of ftpIp
     * @param ftpIp the ftpIp to set
     */
    public void setFtpIp(String ftpIp) {
        this.ftpIp = ftpIp;
    }

    /**
     * Getter of ftpPort
     * @return the ftpPort
     */
    public String getFtpPort() {
        return ftpPort;
    }

    /**
     * Setter of ftpPort
     * @param ftpPort the ftpPort to set
     */
    public void setFtpPort(String ftpPort) {
        this.ftpPort = ftpPort;
    }

    /**
     * Getter of ftpPath
     * @return the ftpPath
     */
    public String getFtpPath() {
        return ftpPath;
    }

    /**
     * Setter of ftpPath
     * @param ftpPath the ftpPath to set
     */
    public void setFtpPath(String ftpPath) {
        this.ftpPath = ftpPath;
    }

    /**
     * Getter of ftpUserName
     * @return the ftpUserName
     */
    public String getFtpUserName() {
        return ftpUserName;
    }

    /**
     * Setter of ftpUserName
     * @param ftpUserName the ftpUserName to set
     */
    public void setFtpUserName(String ftpUserName) {
        this.ftpUserName = ftpUserName;
    }

    /**
     * Getter of ftpPwd
     * @return the ftpPwd
     */
    public String getFtpPwd() {
        return ftpPwd;
    }

    /**
     * Setter of ftpPwd
     * @param ftpPwd the ftpPwd to set
     */
    public void setFtpPwd(String ftpPwd) {
        this.ftpPwd = ftpPwd;
    }

    /**
     * Getter of ftpDownloadInterval
     * @return the ftpDownloadInterval
     */
    public String getFtpDownloadInterval() {
        return ftpDownloadInterval;
    }

    /**
     * Setter of ftpDownloadInterval
     * @param ftpDownloadInterval the ftpDownloadInterval to set
     */
    public void setFtpDownloadInterval(String ftpDownloadInterval) {
        this.ftpDownloadInterval = ftpDownloadInterval;
    }

    /**
     * Getter of ftpUploadInterval
     * @return the ftpUploadInterval
     */
    public String getFtpUploadInterval() {
        return ftpUploadInterval;
    }

    /**
     * Setter of ftpUploadInterval
     * @param ftpUploadInterval the ftpUploadInterval to set
     */
    public void setFtpUploadInterval(String ftpUploadInterval) {
        this.ftpUploadInterval = ftpUploadInterval;
    }

}
