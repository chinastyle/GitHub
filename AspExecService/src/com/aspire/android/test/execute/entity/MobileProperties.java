package com.aspire.android.test.execute.entity;

import com.thoughtworks.xstream.annotations.XStreamAlias;

/**
 * The class <code>MobileInfo</code>
 *
 * @author likai
 * @version 1.0
 */
public class MobileProperties {

    @XStreamAlias("deviceName")
    private String deviceName;

    @XStreamAlias("mobileNum")
    private String mobileNum;

    @XStreamAlias("deviceType")
    private String deviceType;

    @XStreamAlias("imei")
    private String imei;

    @XStreamAlias("ip")
    private String ip;

    @XStreamAlias("provinceCode")
    private String provinceCode;

    @XStreamAlias("cityCode")
    private String cityCode;

    @XStreamAlias("companyCode")
    private String companyCode;

    @XStreamAlias("tester")
    private String tester;

    /**
     * Getter of deviceName
     * @return the deviceName
     */
    public String getDeviceName() {
        return deviceName;
    }

    /**
     * Setter of deviceName
     * @param deviceName the deviceName to set
     */
    public void setDeviceName(String deviceName) {
        this.deviceName = deviceName;
    }

    /**
     * Getter of mobileNum
     * @return the mobileNum
     */
    public String getMobileNum() {
        return mobileNum;
    }

    /**
     * Setter of mobileNum
     * @param mobileNum the mobileNum to set
     */
    public void setMobileNum(String mobileNum) {
        this.mobileNum = mobileNum;
    }

    /**
     * Getter of deviceType
     * @return the deviceType
     */
    public String getDeviceType() {
        return deviceType;
    }

    /**
     * Setter of deviceType
     * @param deviceType the deviceType to set
     */
    public void setDeviceType(String deviceType) {
        this.deviceType = deviceType;
    }

    /**
     * Getter of imei
     * @return the imei
     */
    public String getImei() {
        return imei;
    }

    /**
     * Setter of imei
     * @param imei the imei to set
     */
    public void setImei(String imei) {
        this.imei = imei;
    }

    /**
     * Getter of ip
     * @return the ip
     */
    public String getIp() {
        return ip;
    }

    /**
     * Setter of ip
     * @param ip the ip to set
     */
    public void setIp(String ip) {
        this.ip = ip;
    }

    /**
     * Getter of provinceCode
     * @return the provinceCode
     */
    public String getProvinceCode() {
        return provinceCode;
    }

    /**
     * Setter of provinceCode
     * @param provinceCode the provinceCode to set
     */
    public void setProvinceCode(String provinceCode) {
        this.provinceCode = provinceCode;
    }

    /**
     * Getter of cityCode
     * @return the cityCode
     */
    public String getCityCode() {
        return cityCode;
    }

    /**
     * Setter of cityCode
     * @param cityCode the cityCode to set
     */
    public void setCityCode(String cityCode) {
        this.cityCode = cityCode;
    }

    /**
     * Getter of companyCode
     * @return the companyCode
     */
    public String getCompanyCode() {
        return companyCode;
    }

    /**
     * Setter of companyCode
     * @param companyCode the companyCode to set
     */
    public void setCompanyCode(String companyCode) {
        this.companyCode = companyCode;
    }

    /**
     * Getter of tester
     * @return the tester
     */
    public String getTester() {
        return tester;
    }

    /**
     * Setter of tester
     * @param tester the tester to set
     */
    public void setTester(String tester) {
        this.tester = tester;
    }
}
